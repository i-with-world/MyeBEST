//在指定位置显示一个字符,包括部分字符
//x:0~127
//y:0~63
//mode:0,反白显示;1,正常显示				 

void OLED_Write_Char(int8u_t x,int8u_t y,int8u_t chr,int8u_t mode)
{      			    
   			    
    int8u_t t;
    chr=chr-' ';//得到偏移后的值			   
    for(t=0;t<6;t++)//按行写入，一共写size行
    {   
        if(mode) 
        {
            Display_GRAM[x+t][y/8]=ASCII_0608[chr][t];  //调用0608字体
        }
        else //反白显示
        {
            Display_GRAM[x+t][y/8]=~ASCII_0608[chr][t];  //调用0608字体
        }	
     }
}

