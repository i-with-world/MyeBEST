 /*********************************************************
  * 函数名：void Dis_Number
  * 入口参数：x(0~121):数字的位置
  * 		y(0~56):数字的行数
  * 		num(0~65535):要显示的数字
  * 作用：定位显示数字
  ***********************************************************/
void Dis_Number(int8u_t x, int8u_t y, int16u_t num)
{
    signed   char i;
    unsigned char ii;
    unsigned char dat[6];

    for(i = 0; i < 6; i++) dat[i] = 0; 	//初始化数据
    i = 0;
    while(num / 10)						//拆位
    {
            dat[i] = num % 10;				//最低位
            num /= 10; 
            i++;
    }
    dat[i] = num;					//最高位
    ii = i;								//保存dat的位数
    for(; i >= 0; i--)	dat[i] += 48;	//转化成ASCII
    
    for(i = ii; i >= 0; i--)
      OLED_Write_Char(x+=6, y, dat[i],1);			//输出数值
}

